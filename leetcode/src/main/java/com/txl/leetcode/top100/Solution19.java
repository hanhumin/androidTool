/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode() {}
 * ListNode(int val) { this.val = val; }
 * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution19 {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        if(head == null){
            return null;
        }
        ListNode h = head;
        ListNode pre = head;
        int count = 0;
        while (pre!=null){
            if(count>n){
                h = h.next;
            }
            pre = pre.next;
            count++;
        }
        if(count>n){//已经走动需要删除
            ListNode temp = h.next;
            h.next = temp.next;
        }else if(count == n){//要删除的是头指针
            head = h.next;
        }
        return head;
    }

    public static class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }
}
